'''
Types of Sorting algorithms
'''
# SPACCE USED:
# - in place => Sorting algorithms whic does not require any extra space for soring (example bubble sort)
# - out of place => Sorting algorithms which requires an extra space for sorting (example merge sort)
# STABILITY:
# - Stable => if a sorting algorithm after sorting the contents does not change the sequence of similar content i nwhich thay appear, then this sorting is called stable sorting (example insertion sort)
# - Unstable => if a sorting algorithm after sorting the content changes the sequence of similar content in which they appear, then it is called ustable sort.


'''
Sorting Terminology
'''
# - Increasing order => if successive element is greater than the previous one (example: 1,3,5,7)
# - Decreasing order => if successive element is less than the previous one (example: 11,9,7,5,3,1)
# - Non increasing order => if successive element is less than or equal to its previous element in the sequence (example: 11,9,5,5,3,1)
# - Non decreasing order => if successive element is greater then or equal to its previous element in the sequence (example: 1,4,4,7,8)


