


'''
What is Divide and Conquer Algorithm
'''
# Divide and conquer is an algorithm design paradigm which works by recursively breaking down a 
# problem into subproblems of similar type, until these become simple enough to be solved directly. 
# The solutions to the subproblems are then combined to give a solution to the original problem

# Example: Developing a website
# divide website for smaller modules:
# -module 1
#   -submodel 1
#   -submodel 2
# -module 2
#   -submodel 3
#   -submodel 4
# -module 3
#   -submodel 5
#   -submodel 6


'''
Property of Divide and Conquer Algorithm
'''
# OPTIMAL SUBSTRUCTURE:
#   - If any problem's overall optimal solution can be constructed from the optimal solutions of its subproblem
#     then this problem has optimal substructure

'''
Why we need it?
'''
# It is very effictive when the problem has optimal substructure property.


